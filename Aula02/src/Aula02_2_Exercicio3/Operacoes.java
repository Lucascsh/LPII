
package Aula02_2_Exercicio3;

public class Operacoes {
    public int valor1;
    public int valor2;
    public int Soma(int v1, int v2){
        this.valor1 = v1;
        this.valor2 = v2;
        return v1+v2;
    }
    public int Subtracao(int v1, int v2){
        this.valor1 = v1;
        this.valor2 = v2;
        return v1-v2;
    }
    public int Multiplicacao(int v1, int v2){
        this.valor1 = v1;
        this.valor2 = v2;
        return v1*v2;
    }
    public int Divisao(int v1, int v2){
        this.valor1 = v1;
        this.valor2 = v2;
        return v1/v2;
    }
    
}
